window.i18n = i18nDict;

window.applyTranslations = function(lang) {
    if(!window.i18n[lang]) return;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if(window.i18n[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = window.i18n[lang][key];
            } else {
                el.textContent = window.i18n[lang][key];
            }
        }
    });
};

window.t = function(key, lang) {
    if(!lang) lang = window.currentLang || 'es';
    return window.i18n[lang] && window.i18n[lang][key] ? window.i18n[lang][key] : key;
};
